for (const [rel, oldText, newText] of PATCHES) {
  const file = path.join(ROOT, rel)
  if (!fs.existsSync(file)) {
    console.warn(`  [skip] ${rel} (file not found)`)
    totalSkipped++
    continue
  }
  const src = fs.readFileSync(file, 'utf8')
  const occurrences = src.split(oldText).length - 1
  if (occurrences === 0) {
    console.warn(`  [skip] ${rel} (pattern not found)`)
    totalSkipped++
    continue
  }
  if (occurrences > 1) {
    console.error(`  [ERROR] ${rel} (pattern found ${occurrences} times — needs more context)`)
    totalErrors++
    continue
  }
  const next = src.replace(oldText, newText)
  fs.writeFileSync(file, next, 'utf8')
  console.log(`  [updated] ${rel}`)
  totalChanges++
}

console.log(`\nDone. ${totalChanges} patches applied, ${totalSkipped} skipped, ${totalErrors} errors.`)
